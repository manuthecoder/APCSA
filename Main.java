public class Main {
    public static void main(String[] args) {
        byte byteOne = 127;
        short shortOne = 32000;
        int intOne = 555;
        long longOne = 100000L;
        float floatOne = 5.5f;
        double doubleOne = 10.55;
        char charOne = 'A';
        boolean booleanOne = true;
        String stringOne = "Hello";

        System.out.println("byteOne is = " + byteOne);
        System.out.println("shortOne is = " + shortOne);
        System.out.println("intOne is = " + intOne);
        System.out.println("longOne is = " + longOne);
        System.out.println("floatOne is = " + floatOne);
        System.out.println("doubleOne is = " + doubleOne);
        System.out.println("charOne is = " + charOne);
        System.out.println("booleanOne is = " + booleanOne);
        System.out.println("stringOne is = " + stringOne);
    }
}
